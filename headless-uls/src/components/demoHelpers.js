/**
 * Merge langlinks info into language objects
 *
 * @param {Array} languages - Array of language objects (with code)
 * @param {Array} langlinks - Array of langlink objects ({ lang, "*" })
 * @return {Array} New array of language objects with "info" added
 */
const mergeLangInfo = ( languages, langlinks ) => {
	if ( !Array.isArray( languages ) || !Array.isArray( langlinks ) ) {
		throw new TypeError( 'Expected arrays for languages and langlinks' );
	}

	// Create a fast lookup map for langlinks
	const linkMap = new Map( langlinks.map( ( link ) => [ link.lang, link[ '*' ] ] ) );

	// Map over languages and attach info if available
	return languages.map( ( lang ) => ( {
		// eslint-disable-next-line es-x/no-rest-spread-properties
		...lang,
		info: linkMap.get( lang.code ) || null
	} ) ).filter( ( lang ) => lang.info !== null );
};

const assignAttributeToRandomLanguages =
	( languages, property, value, threshold = 0.3 ) => languages.map( ( lang ) => {
		const random = Math.random();

		if ( random < threshold ) {
			return {
			// eslint-disable-next-line es-x/no-rest-spread-properties
				...lang,
				[ property ]: value
			};
		}

		return lang;
	} );

const groupLanguagesByGroup = ( languages, groupTitles = {} ) => {
	const groupsMap = new Map();

	for ( const lang of languages ) {
		// eslint-disable-next-line es-x/no-rest-spread-properties
		const { groups: langGroups = [], ...subLang } = lang;

		for ( const groupCode of langGroups ) {
			if ( !groupsMap.has( groupCode ) ) {
				groupsMap.set( groupCode, {
					code: groupCode,
					title: groupTitles[ groupCode ] || groupCode,
					languages: []
				} );
			}
			groupsMap.get( groupCode ).languages.push( subLang );
		}
	}

	// Sort languages alphabetically within each group (optional)
	for ( const group of groupsMap.values() ) {
		group.languages.sort( ( a, b ) => a.autonym.localeCompare( b.autonym ) );
	}

	const groups = Array.from( groupsMap.values() );
	const groupOrder = Object.keys( groupTitles );
	return groups.sort( ( a, b ) => {
		const aIndex = groupOrder.indexOf( a.code );
		const bIndex = groupOrder.indexOf( b.code );

		// If a code is missing, put it at the end (indexOf returns -1)
		// eslint-disable-next-line max-len
		return ( aIndex === -1 ? groupOrder.length : aIndex ) - ( bIndex === -1 ? groupOrder.length : bIndex );
	} );
};

const flattenLanguageGroups = ( groups, unique = true ) => {
	if ( !Array.isArray( groups ) ) {
		throw new TypeError( 'Expected an array of groups' );
	}

	// eslint-disable-next-line es-x/no-array-prototype-flat
	const allLangs = groups.flatMap( ( group ) => group.languages || [] );

	if ( !unique ) {
		return allLangs;
	}

	// Deduplicate by `code`
	const seen = new Map();
	for ( const lang of allLangs ) {
		if ( !seen.has( lang.code ) ) {
			seen.set( lang.code, lang );
		}
	}
	return Array.from( seen.values() );
};

